package org.narrowingWindeningEx;

public class ByteEx1 {

	public static void main(String[] args) {
		int a =255;
		byte b =(byte)a;
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		
		int a1 =-255;
		byte b1 = (byte)a1;
		System.out.println("a = "+a);
		System.out.println("b = "+b1);
	}
}

