//****************************DO WHILE LOOP*****************************//
package org.loops.Ex1;
public class WhileEx1 {
	public static void main(String[] args) {
		System.out.println("**Program Starts**");
		int i=1;
		do{
			System.out.println(i);
			i++;
			}while (i<11);
		System.out.println("i = "+i);
		System.out.println("**Program Ends**");
	}
}