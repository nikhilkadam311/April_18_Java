package org.loops.Ex1;
public class ForEx1 {
	public static void main(String[] args) {
		System.out.println("**Program Starts**");
		for (int a=11,b=21,c=51; a<=13; a++,b--,c++) {
			System.out.println(a+", "+b+", "+c);
		}	
		System.out.println("**Program Ends**");
	}
}