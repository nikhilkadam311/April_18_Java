//****************************WHILE LOOP*****************************//
package org.loops.Ex1;
public class WhileEx2 {
	public static void main(String[] args) {
		System.out.println("**Program Starts**");
		int i=1;
		while (i<11){
			System.out.println(i);
			i++;
			}
		System.out.println("i = "+i);
		System.out.println("**Program Ends**");
	}
}